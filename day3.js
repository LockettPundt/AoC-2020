const skiSlope = (arr) => {
  let slopePosition = 0
  return arr.reduce((sum, x, i) => {
    if (slopePosition >= x.length) {
      slopePosition = slopePosition - x.length
    }
    if (x[slopePosition] === '#') {
      sum += 1
    }
    slopePosition += 3
    return sum
  }, 0)
}

